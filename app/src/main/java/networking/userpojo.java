package networking;

public class userpojo
{




    String name;
    String cla;

    public userpojo(String name, String cla) {
        this.name = name;
        this.cla = cla;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCla() {
        return cla;
    }

    public void setCla(String cla) {
        this.cla = cla;
    }
}
